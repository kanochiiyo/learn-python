print("Program Menghitung Luas Segitiga\n")

# User input nilai alas dan tinggi
alas = float(input("Masukkan nilai panjang alas = "))
tinggi = float(input("Masukkan nilai tinggi = "))

# Hitung luas segitiga 
luas_segitiga = 0.5 * alas * tinggi 

# Tampilkan nilai luas segitiga
print(f"Luas segitiga = {luas_segitiga}") # Gunakan f-string